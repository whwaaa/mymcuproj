/*
* 功能: 定时器3,us级延时
*	参数: xus, 范围0~65535us
*/
void Delay_Us(uint16_t t){
  uint16_t counter = 0;
	TIM3->ARR = t;//设置定时器自动重装载的值, 到该值后重新计数
	TIM3->CNT = 0;//设置定时器初始值
	TIM3->CR1 |= TIM_CR1_CEN;//启动定时器
	while ( counter != t ) {
		counter = TIM3->CNT;//获取定时器当前计数值
	}
	TIM3->CR1 &= (uint16_t)(~((uint16_t)TIM_CR1_CEN));//停止定时器
}
void Delay_Ms(uint16_t xms){
	while( xms-- ) {
		Delay_Us(1000);
	}	
}
